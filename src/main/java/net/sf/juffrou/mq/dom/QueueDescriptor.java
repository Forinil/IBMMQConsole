package net.sf.juffrou.mq.dom;

public class QueueDescriptor {

	private String name;
	private Integer dept;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getDept() {
		return dept;
	}

	public void setDept(Integer dept) {
		this.dept = dept;
	}
}
